/**
 * Represents a country.
 */

export class Country {

  /**
   * The unique identifier for the country.
   * @type {number}
   */
  id!: number;

  /**
   * The code of the country.
   * @type {string}
   */
  code!: string;

  /**
   * The name of the country.
   * @type {string}
   */
  name!: string;
}
